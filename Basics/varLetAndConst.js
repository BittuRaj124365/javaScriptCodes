// var means you can consider a variable than can be change.
var a=10;
console.log(a); // 10
a=32;
console.log(a);  // 32
console.log(++a);   // 33

// const means you can consider a constant which is fixed

const b=4;
console.log(b); // 4
b=3;
console.log(b); // it will throw an error  because b can be use only once for whole code. We cannot change or update b